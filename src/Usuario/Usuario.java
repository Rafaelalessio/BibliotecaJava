package Usuario;

public class Usuario {
    //declarando os atributos
    public String nome ;
    public String email ;
    public int telefone;
    //criando o construtor da classe usuario com os atributos a cima
    public Usuario(String nome, String email, int telefone) {
        this.nome = nome;
        this.email = email;
        this.telefone = telefone;
    }
}

